<?php

    // String
    $a = "hello";
    var_dump($a);

    // Integer
    $b = 10;
    var_dump($b);

    // Floating
    $c = 20.56;
    var_dump($c);

    // Array
    $d = array("Football", "Cricket", "Hockey");
    var_dump($d);

    // Null
    $e = null;
    var_dump($e);

    // Boolean
    $f = true;
    var_dump($f);
    
?>