<?php
    // Arithmetic Operator
    $x = 10;
    $y = 20;

    echo $x + $y . "\n"; // Addition
    echo $x - $y . "\n"; // Subtraction
    echo $x * $y . "\n"; // Multiplication
    echo $x / $y . "\n"; // Division

    // Assignment Operator
    $x += 50;
    $x -= 10;

    // Comparision Operator
    echo $x > $y . "\n"; //  Greater Than
    echo $x < $y . "\n"; //  Less Than 
    echo $x == $y . "\n"; //  Double Equal 
    echo $x != $y . "\n"; //  Not Equal
    echo $x >= $y . "\n"; //  Greater Than Equal
    echo $x <= $y . "\n"; //  Less Than Equal 

    // Logical Operator
    echo $x and $y . "\n"; //  Logical And
    echo $x or $y . "\n"; //  Logical Or
    echo !$x . "\n"; //  Logical Not

    // Increment / Decrement Operator
    echo ++$x . "\n"; //  Pre Increment
    echo $x++ . "\n"; //  Post Increment
    echo --$y . "\n"; //  Pre Decrement
    echo $y-- . "\n"; //  Post Decrement
?>